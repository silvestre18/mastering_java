// This file will explain all the features of the statement do while
class statementdowhile
{
  public static void main(String aa[])
  {
    /* A simple example of the use of do while */
    
    int a =5;
    do
    {
      System.out.print(a +" ");
      a--;
      /* This will print 
       5 4
      */
    
    }while(a>3);      // the do while guarantee to execute the body at least one time
    
    // Do not forget the ; at the end of while
  
  }

}